public class Main {
    public static StringBuffer decipher(String text, int s) {
        StringBuffer result= new StringBuffer();
        for (int i=0; i<text.length(); i++) {
            char ch;
            if (Character.isUpperCase(text.charAt(i))) ch = (char)(((int)text.charAt(i)+s-65)%26+65);
            else ch = (char)(((int)text.charAt(i)+s-97)%26+97);
            result.append(ch);
        }
        return result;
    }

    public static void main(String[] args) {
        String text = "TEST";
        int collumn = 1;
        for (int shift=1;shift<=26;shift++){
            String shifted = String.valueOf(decipher(text,shift));
            if (collumn<=2) System.out.print("Shift "+shift+": "+shifted+"  ");
            else {
                System.out.println("Shift "+shift+": "+shifted);
                collumn=0;
            }
            collumn++;
        }
    }
}
